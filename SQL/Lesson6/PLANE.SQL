CREATE TABLE PLANE(
    ID NUMBER PRIMARY KEY,
    MODEL NVARCHAR2(50),
    CODE NVARCHAR2(50) UNIQUE,
    YEAR_PRODUCED NUMBER,
    AVG_FUEL_CONSUMPTION NUMBER(5,2)
);

CREATE SEQUENCE PLANE_SEQ MINVALUE 1 INCREMENT BY 1;

CREATE TABLE FLIGHT(
    ID NUMBER PRIMARY KEY,
    PLANE_ID NUMBER,
    CONSTRAINT PLANE_FK FOREIGN KEY(PLANE_ID) REFERENCES PLANE(ID),
    DATE_FLIGHT TIMESTAMP,
    CITY_FROM NVARCHAR2(50),
    CITY_TO NVARCHAR2(50)
);

CREATE SEQUENCE FLIGHT_SEQ MINVALUE 1 INCREMENT BY 1;

CREATE TABLE PASSENGER(
    ID NUMBER PRIMARY KEY,
    LAST_NAME NVARCHAR2(50),
    NATIONALITY NVARCHAR2(50),
    DATE_OF_BIRTH TIMESTAMP
);

CREATE SEQUENCE PASSENGER_SEQ MINVALUE 1 INCREMENT BY 1;

CREATE TABLE FLIGHT_PASSENGER (
  FLIGHT_ID NUMBER,
  CONSTRAINT FLIGHT_FK FOREIGN KEY(FLIGHT_ID) REFERENCES FLIGHT(ID),
  PASSENGER_ID NUMBER,
  CONSTRAINT PASSENGER_FK FOREIGN KEY(PASSENGER_ID) REFERENCES PASSENGER(ID)
);